class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	  Scanner sc = new Scanner(System.in);
	  int T = sc.nextInt();
	  
	  for (int Test = 0 ; Test < T ; Test++)
	  {
	      int X = sc.nextInt();
	      int N = sc.nextInt();
	      
	      
	      
	      int Z = X/10;
	      
	      int Y = Z * N;
	      
	      System.out.println(Y);
	  }
	}
}
